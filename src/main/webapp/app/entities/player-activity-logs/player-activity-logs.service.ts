import axios from 'axios';

import { type IPlayerActivityLogs } from '@/shared/model/player-activity-logs.model';

const baseApiUrl = 'api/player-activity-logs';

export default class PlayerActivityLogsService {
  public find(id: string): Promise<IPlayerActivityLogs> {
    return new Promise<IPlayerActivityLogs>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: string): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IPlayerActivityLogs): Promise<IPlayerActivityLogs> {
    return new Promise<IPlayerActivityLogs>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IPlayerActivityLogs): Promise<IPlayerActivityLogs> {
    return new Promise<IPlayerActivityLogs>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IPlayerActivityLogs): Promise<IPlayerActivityLogs> {
    return new Promise<IPlayerActivityLogs>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
